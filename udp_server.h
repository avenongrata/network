#ifndef UDP_SERVER_H
#define UDP_SERVER_H

#include <string.h>
#include <iostream>

//-----------------------------------------------------------------------------

namespace server
{

//=============================================================================

typedef int sockfd;

//-----------------------------------------------------------------------------

class Udp
{
public:
    //-------------------------------------------------------------------------

    Udp();
    ~Udp();

    //-------------------------------------------------------------------------

    // using one thread
    int serial();

    // using multiple threads
    int multiThread();

    // echo server
    int echo();

    // retranslator server
    int retranslator();

    //------------------------------------------------------------------------

private:
    //-------------------------------------------------------------------------

    sockfd fd;
    std::string ip;
    int port;

    //-------------------------------------------------------------------------

    sockfd m_create();
    int m_close();
};

//=============================================================================

} // namespace server

#endif // UDP_SERVER_H
